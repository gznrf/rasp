from gpiozero import Servo
from time import sleep

# Подключаем сервопривод к пину GPIO (например, GPIO17)
servo = Servo(17)  # замените 17 на ваш GPIO пин

print("Начало диагностики MG90S...")

try:
    # Проверка центрального положения
    print("Перемещаем в центральное положение...")
    servo.value = 0  # центральное положение
    sleep(1)

    # Проверка крайнего левого положения
    print("Перемещаем в левое положение...")
    servo.value = -1  # левый край
    sleep(1)

    # Проверка крайнего правого положения
    print("Перемещаем в правое положение...")
    servo.value = 1  # правый край
    sleep(1)

    # Возврат в центральное положение
    print("Возврат в центральное положение...")
    servo.value = 0
    sleep(1)

    print("Диагностика завершена успешно!")

except KeyboardInterrupt:
    print("Диагностика прервана пользователем.")

finally:
    # Отключаем сигнал
    servo.value = None
    print("Сервопривод отключен.")
